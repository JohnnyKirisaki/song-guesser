const fs = require('fs');
const path = require('path');

// Minimal 1-frame MP3 (Silence)
const dummyMp3Params = [
    0xFF, 0xE3, 0x18, 0xC4, 0x00, 0x00, 0x00, 0x03, 0x48, 0x00, 0x00, 0x00, 0x00, 0x4C, 0x41, 0x4D,
    0x45, 0x33, 0x2E, 0x39, 0x38, 0x2E, 0x32, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00
];
const buffer = Buffer.from(dummyMp3Params);

const soundsDir = path.join(process.cwd(), 'public', 'sounds');
const files = ['correct.mp3', 'wrong.mp3', 'tick.mp3', 'reveal.mp3', 'win.mp3'];

if (!fs.existsSync(soundsDir)) {
    fs.mkdirSync(soundsDir, { recursive: true });
}

files.forEach(file => {
    fs.writeFileSync(path.join(soundsDir, file), buffer);
    console.log(`Created dummy sound: ${file}`);
});
